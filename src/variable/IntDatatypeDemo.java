package variable;

public class IntDatatypeDemo {
    public static void main(String[] args) {
        byte a; //byte 1 byte
        a = 127;
        short b; //short 2 byte
        b = 32_767;
        int c; //int 4 byte
        c = 2_147_483_647;
        long d; //long 8 byte
        d = 9_223_372_036_854_775_807L; //long는 숫자 마지막에 L부처야됨
    }
}
